// 編碼後的手牌 => 編碼後的雀頭面子拆解（七對子單獨設置在一個比特位上；國士未算在內）
// 拆解是一個數組，因為同一個手牌可能會有多種不同的拆解方式
// len(winTable) == 9362
var winTable = map[int][]int{}

func init() {
	pnc := func(er error) {
		if er != nil {
			panic(er)
		}
	}

	sDec, err := base64.StdEncoding.DecodeString(agariData)
	pnc(err)

	zipRd, err := zip.NewReader(bytes.NewReader(sDec), int64(len(sDec)))
	pnc(err)

	if len(zipRd.File) == 0 {
		pnc(fmt.Errorf("錯誤的壓縮數據"))
	}

	f := zipRd.File[0]
	rc, err := f.Open()
	pnc(err)

	data, err := ioutil.ReadAll(rc)
	lines := strings.Split(string(data), "\n")
	if len(lines) != 9362 {
		pnc(fmt.Errorf("錯誤的數據 %d", len(lines)))
	}

	for _, line := range lines {
		splits := strings.Split(line, " ")
		if len(splits) < 2 {
			pnc(fmt.Errorf("錯誤的數據 %s", line))
		}
		key, err := strconv.Atoi(splits[0])
		pnc(err)
		divides := make([]int, len(splits)-1)
		for i, divide := range splits[1:] {
			divides[i], err = strconv.Atoi(divide)
			pnc(err)
		}
		winTable[key] = divides
	}
}

